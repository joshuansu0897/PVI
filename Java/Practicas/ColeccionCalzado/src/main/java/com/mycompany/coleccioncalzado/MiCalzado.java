    private void disableAll() {
        lsTipo.setEnabled(false);
        rbCaballero.setEnabled(false);
        rbDama.setEnabled(false);
        txtMarca.setEnabled(false);
        txtPrecio.setEnabled(false);
        btnGuardar.setEnabled(false);
        btnCancelar.setEnabled(false);
        cbNumero.setEnabled(false);

        table.setEnabled(true);
    }

    private void limpiar() {
        lsTipo.setSelectedIndex(0);

        txtMarca.setText(null);
        txtPrecio.setText(null);
    }

    private void enableAll() {
        lsTipo.setEnabled(true);
        rbCaballero.setEnabled(true);
        rbDama.setEnabled(true);
        txtMarca.setEnabled(true);
        txtPrecio.setEnabled(true);
        btnGuardar.setEnabled(true);
        btnCancelar.setEnabled(true);
        cbNumero.setEnabled(true);

        table.setEnabled(false);
    }

    private void setearDatos() {
        int index = table.getSelectedRow();
        if (index == -1) {
            return;
        }
        txtMarca.setText(mdl.getValueAt(index, 0).toString());
        lsTipo.setSelectedValue(mdl.getValueAt(index, 1), false);
        cbNumero.setSelectedItem(mdl.getValueAt(index, 2));

        rbCaballero.setSelected(mdl.getValueAt(index, 3).equals("Caballero"));
        rbDama.setSelected(mdl.getValueAt(index, 3).equals("Dama"));

        txtPrecio.setText(mdl.getValueAt(index, 4).toString());
    }

