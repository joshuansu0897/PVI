    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int index = table.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Selecciones que desea Editar");
            return;
        }
        enableAll();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (table.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecciones que desea Eliminar");
            return;
        }

        mdl.removeRow(table.getSelectedRow());
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int index = table.getSelectedRow();

        if ("".equals(txtMarca.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Marca no valida");
            return;
        }

        String damaCaballero = rbDama.isSelected() ? "Dama" : "Caballero";

        if (!txtPrecio.getText().matches("([0-9,.]{1,})")) {
            JOptionPane.showMessageDialog(this, "Precio no valido");
            return;
        }

        if (index == -1) {
            mdl.addRow(new Object[]{
                txtMarca.getText(),
                lsTipo.getSelectedValue(),
                Float.parseFloat(cbNumero.getSelectedItem().toString()),
                damaCaballero,
                Double.parseDouble(txtPrecio.getText())
            });
        } else {
            mdl.setValueAt(txtMarca.getText(), index, 0);
            mdl.setValueAt(lsTipo.getSelectedValue(), index, 1);
            mdl.setValueAt(Float.parseFloat(cbNumero.getSelectedItem().toString()), index, 2);
            mdl.setValueAt(damaCaballero, index, 3);
            mdl.setValueAt(Double.parseDouble(txtPrecio.getText()), index, 4);
        }

        limpiar();
        disableAll();
    }//GEN-LAST:event_btnGuardarActionPerformed

