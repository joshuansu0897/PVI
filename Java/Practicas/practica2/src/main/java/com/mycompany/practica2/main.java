    private String operacion() {
        if (id.getText().trim().equals("")) {
            return "";
        }

        if (!cantidad.getText().trim().matches("^(?:[1-9]|0[1-9]|10)$")) {
            return "";
        }

        int ca = Integer.parseInt(cantidad.getText().trim());
        int pe = Integer.parseInt(precio.getText().trim());

        return String.valueOf(ca * pe);
    }

