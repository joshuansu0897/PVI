    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String impo = operacion();

        if (impo.equals("")) {
            return;
        }

        dtm.addRow(new Object[]{
            id.getText(),
            nombre.getText(),
            precio.getText(),
            cantidad.getText(),
            impo
        });
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tablaProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaProductosMouseClicked
        int index = tablaProductos.getSelectedRow();
        if (index != -1) {
            id.setText(tablaProductos.getValueAt(index, 0).toString());
            nombre.setText(tablaProductos.getValueAt(index, 1).toString());
            precio.setText(tablaProductos.getValueAt(index, 2).toString());
            String imp = operacion();
            importe.setText(imp);
        }
    }//GEN-LAST:event_tablaProductosMouseClicked

