    private void llenarTabla() {
        dtm.setRowCount(0);
        cds.forEach(c -> {
            dtm.addRow(new Object[]{
                c.getTitle(),
                c.getArtist(),
                c.getCompany(),
                c.getCountry(),
                c.getPrice(),
                c.getYear()
            });
        });
    }

