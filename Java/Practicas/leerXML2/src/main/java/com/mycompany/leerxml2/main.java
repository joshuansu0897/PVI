    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String path = Util.fileChooser("");
        if (path.trim().equals("")) {
            return;
        }
        
        try {
            ArrayList<CD> tmp = ReadXML.readXML(path);
            int selecion = JOptionPane.showConfirmDialog(this, "Desea concatenar el contenido del JSON a la tabla?");
            if (selecion == JOptionPane.YES_OPTION) {
                cds.addAll(tmp);
            } else if (selecion == JOptionPane.NO_OPTION) {
                cds.clear();
                cds.addAll(tmp);
            }

            llenarTabla();
        } catch (Exception ex) {
            System.out.println("Error al leer el documento");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

